package Day1;

public class ArithmeticOperations {

	public static void main(String[] args) {
		int num1=20;
		int num2=4;
		System.out.println("Addition of above nums is "+(num1+num2));
		System.out.println("subtraction of above nums is "+(num1-num2));
		System.out.println("product of above nums is "+(num1*num2));
		System.out.println("Division of above nums is "+(num1/num2));
		System.out.println("Reminder of above nums is "+(num1%num2));
	}

}
